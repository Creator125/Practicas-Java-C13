package calculadora;

public class Operacion {
    int num1, num2;
    
    public int sumar(){
        return num1 + num2;
    }
    
    public int restar(){
        return num1 - num2;
    }
    
    public int multiplicar(){
        return num1 * num2;
    }
    
    public int dividir(){
        return num1 / num2;
    }
}
